/* ─── Counter animation ─── */
function runCounter(el) {
  const target = parseInt(el.dataset.target, 10);
  gsap.fromTo({ val: 0 }, { val: target, duration: 1.6, ease: 'power2.out',
    onUpdate() { el.textContent = Math.round(this.targets()[0].val) + (this.progress() === 1 ? '+' : ''); }
  });
}
ScrollTrigger.create({
  trigger: '.stats-row',
  start: 'top 82%',
  once: true,
  onEnter() { document.querySelectorAll('.stat-num').forEach(runCounter); }
});

