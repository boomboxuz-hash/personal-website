/* ===== STATE ===== */
let currentLang = localStorage.getItem('lang') || 'uz';
let typedInstance = null;

/* ===== APPLY LANGUAGE ===== */
function applyLang(lang) {
  const t = i18n[lang];
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  // innerHTML keys (bold tags)
  ['card1_years','card2_years','card3_years'].forEach(key => {
    const el = document.querySelector(`[data-i18n="${key}"]`);
    if (el && t[key]) el.innerHTML = t[key];
  });

  const footerEl = document.querySelector('[data-i18n="footer_copy"]');
  if (footerEl) footerEl.innerHTML = t.footer_copy;

  // "Ko'rish / Смотреть" buttons with icons
  document.querySelectorAll('.proj-view-btn [data-i18n="proj_view"]').forEach(span => {
    span.textContent = t.proj_view;
  });

  // Typed.js
  if (typedInstance) { typedInstance.destroy(); document.getElementById('typed').textContent = ''; }
  typedInstance = new Typed('#typed', {
    strings: t.typed_strings,
    typeSpeed: 55, backSpeed: 35, backDelay: 1800, loop: true, smartBackspace: true,
  });

  document.getElementById('langToggle').textContent = lang === 'uz' ? 'RU' : 'UZ';
  localStorage.setItem('lang', lang);
  currentLang = lang;
}

document.getElementById('langToggle').addEventListener('click', () => {
  applyLang(currentLang === 'uz' ? 'ru' : 'uz');
});

